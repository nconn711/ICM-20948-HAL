static void USART2_Send_16(int16_t* data, int len) {
	uint8_t high;
	uint8_t low;
	for (int i = 0; i < len; i++) {
		high = (data[i] & 0xF0) >> 8;
		low = data[i] & 0x0F;
		LL_USART_TransmitData8(USART2, high);
		while (!LL_USART_IsActiveFlag_TC(USART2));
		LL_USART_TransmitData8(USART2, low);
		while (!LL_USART_IsActiveFlag_TC(USART2));
	}
}

